class Expense:
    def __init__(self, name, amount):
        # Initializing the name attribute
        # 'self' refers to the instance of the class. 'name' is a parameter passed to the constructor.
        # 'self.name' creates an instance variable 'name' and assigns it the value passed to the constructor.
        self.name = name
        
        # Initializing the amount attribute
        # Similar to 'name', 'amount' is a parameter passed to the constructor.
        # 'self.amount' creates an instance variable 'amount' and assigns it the value passed to the constructor.
        self.amount = amount
